-- 2번 형질이 없으면서 1번이나 3번 형질을 가진 대장균 개체 수 찾기
SELECT COUNT(*) AS COUNT
FROM ECOLI_DATA -- 대장균 데이터 테이블
WHERE (GENOTYPE & 2) = 0  -- 2번 형질이 없는 경우
  AND (GENOTYPE & 1 > 0 OR GENOTYPE & 4 > 0);  -- 1번이나 3번 형질이 있는 경우

/*
설명:
1. GENOTYPE & 2 = 0
   - 2번 형질(2^1)이 없는지 확인
   - 비트 AND 연산 결과가 0이면 해당 형질 없음

2. GENOTYPE & 1 > 0
   - 1번 형질(2^0)이 있는지 확인
   - 비트 AND 연산 결과가 0보다 크면 해당 형질 있음

3. GENOTYPE & 4 > 0
   - 3번 형질(2^2)이 있는지 확인
   - 비트 AND 연산 결과가 0보다 크면 해당 형질 있음

예시:
GENOTYPE = 13 (1101₍₂₎)인 경우
- 2번 형질 확인: 13 & 2 = 1101₍₂₎ & 0010₍₂₎ = 0 (없음)
- 1번 형질 확인: 13 & 1 = 1101₍₂₎ & 0001₍₂₎ = 1 (있음)
- 3번 형질 확인: 13 & 4 = 1101₍₂₎ & 0100₍₂₎ = 4 (있음)

테스트 케이스:
INSERT INTO ECOLI_DATA (ID, PARENT_ID, SIZE_OF_COLONY, DIFFERENTIATION_DATE, GENOTYPE) VALUES
(1, NULL, 10, '2019-01-01', 8),   -- 1000₍₂₎ - 4번 형질만
(2, NULL, 2, '2019-01-01', 15),   -- 1111₍₂₎ - 1,2,3,4번 형질
(3, 2, 100, '2020-01-01', 1),     -- 0001₍₂₎ - 1번 형질만
(4, 2, 16, '2020-01-01', 13);     -- 1101₍₂₎ - 1,3,4번 형질

실행 결과:
COUNT
2
*/ 