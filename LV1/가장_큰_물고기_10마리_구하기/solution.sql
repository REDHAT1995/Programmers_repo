-- 가장 큰 물고기 10마리의 ID와 길이를 출력
-- ID: 물고기의 고유 식별자
-- LENGTH: 물고기의 길이(cm)
SELECT   -- ID와 길이 컬럼을 선택
FROM FISH_INFO
WHERE LENGTH IS NOT NULL  -- 길이가 NULL이 아닌 물고기만 선택
ORDER BY LENGTH DESC, ID ASC  -- 길이 내림차순, ID 오름차순 정렬
LIMIT 10;  -- 상위 10개만 선택

